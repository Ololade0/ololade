package StudentGrade;

public class StudentGrade {
    private String [] sudents = new String[4];
    private String [] course = new String[4];
    private int [] grade;
    private int [] totalScoreOfAllStudent;
    private int[] total;
    private int [] position;

    public StudentGrade() {
        this.sudents = sudents;
        this.course = course;
        this.grade = grade;
        this.totalScoreOfAllStudent = totalScoreOfAllStudent;
        this.total = total;
        this.position = position;
    }
}